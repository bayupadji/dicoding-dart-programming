void main(List<String> args) {
  var greetings = 'Hello World! dart is great';
  print(greetings);

  // inisialisasi variabel
  var myage = 20;
  print(myage);

  // deklarasi variabel
  var myage2;
  myage2 = 20;
  print(myage2);

  //data type
  String greetings1 = 'Hello World';
  int myage3 = 20;
  print(greetings1);
  print(myage3);

  //deklarasi tanpa inisialisasi
  var x; //dynamic
  x = 7;
  x = 'dart is great';
  print(x);
}
